#OPERADORES ARITMÉTICOS BÁSICOS

#Suma
resultado_suma = 5 + 6 #11

#Resta
resultado_resta = 6 - 1 # 5

#Multiplicacion
resultado_multiplicacion = 2 * 3 #6

#Divion
resultado_division = 10 / 2 #resultado de un numero float 5.0

#Cociente de la division
resultado_cociente = 10 // 2 # resultado de un numero entero 5

#Modulo (resto de la division)
resultado_modulo = 8 % 2 #0

#Potencia
resultado_potencia = 2 ** 3 #8

print(f"Suma: {resultado_suma}")
print(f"Resta: {resultado_resta}")
print(f"Multiplicacion: {resultado_multiplicacion}")
print(f"Division: {resultado_division:.3f}")
print(f"Cociente: {resultado_cociente}")
print(f"Modulo: {resultado_modulo}")
print(f"Potencia: {resultado_potencia}")