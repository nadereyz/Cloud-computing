#OPERADORES LOGICOS

#Operador AND (Y) --> True si 2 condiciones True
a = True
b = True
resultado_and = a and b


#Operador OR (O) --> True si al menos 1 cond. es True
c = False
d = False
resultado_or = c or d


#Operador NOT (NO) --> True si condicion es falsa
e = False
resultado_not = not e
print(resultado_and)
print(resultado_or)
print(resultado_not)
