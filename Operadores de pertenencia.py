# in: comprueba si está dentro de la lista y devuelve True si lo está o False si no está
vehiculo = "coche"
resultado_in = "a" in vehiculo
resultado_in1 = "e" in vehiculo
print(resultado_in)
print(resultado_in1)

# not in: comprueba que el valor no esté dentro de la lista

animal = "elefante"
resultado_not = "q" not in animal
print(resultado_not)


